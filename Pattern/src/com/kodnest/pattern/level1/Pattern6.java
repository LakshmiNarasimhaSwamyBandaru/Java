package com.kodnest.pattern.level1;

public class Pattern6 {
    public static void main(String[] args) {
        //no.of lines
        for (int i=1;i<=5;i++){
            //no.of *'s
            for (int j=1;j<=5;j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
