public class DataTypes {
    public static void main(String[] args) {
        char a='z';
        byte b=10;
        short c=32700;
        int d=214787;
        long e=2147483650l;
        float f=3.14f;
        boolean h=true;
        double g=44.12123;
        System.out.println(a+" "+b+" "+c+" "+d+" "+e+" "+f+" "+g+" "+h);
        int i;
        int j=100;
        i=60;
        i=300;
        System.out.println(i);
        System.out.println(j);
    }
}
